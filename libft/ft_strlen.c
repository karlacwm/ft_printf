#include "libft.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

// int -- a signed integer type (both +-) for general arithematic
// size_t -- unsigned integer type (only +) for sizes and counts
